    private void txt_jumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_jumlahActionPerformed
        // TODO add your handling code here: //jumlahtiket //txt_jumlah
        //totalharga //belitiket jumlahnya berapa tiket
        belitiket=Integer.parseInt(txt_jumlah.getText());
        harga=Integer.parseInt(txt_harga.getText());
        //totalharga=totalbayar*harga; //totalbayar 1 tiket * jumlahtiket = jumlahtiket * harga 1 tiket
        totalharga=harga*belitiket; 
        txt_totalharga.setText(String.valueOf(totalharga));
    }//GEN-LAST:event_txt_jumlahActionPerformed

    private void txt_totalbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_totalbayarActionPerformed
        // TODO add your handling code here://totalbayar //txt_totalbayar
        //input jumlah tiket dan hitung total pembayaran
        totalbayar=Integer.parseInt(txt_totalbayar.getText());
        kembalian=Integer.parseInt(txt_totalharga.getText());
        totalharga=totalbayar-kembalian;
        txt_kembalian.setText(String.valueOf(totalharga));
    }//GEN-LAST:event_txt_totalbayarActionPerformed

    private void txt_jumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_jumlahKeyReleased
        // TODO add your handling code here: //totalharga = harga * jumlah, fungsinya untuk mengambil input
        int htiket;
        int jtiket;
        int ttiket;
        htiket=Integer.valueOf(txt_harga.getText());
        jtiket=Integer.valueOf(txt_jumlah.getText());
        ttiket=htiket*jtiket;
        txt_totalharga.setText(""+ttiket);
    }//GEN-LAST:event_txt_jumlahKeyReleased

    private void txt_totalbayarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_totalbayarKeyReleased
        // TODO add your handling code here: kembalian = totalbayar - totalharga, fungsinya untuk mengambil input
        //bayar-total = kembalian 
        int htiket;
        int jtiket;
        int ttiket;
        htiket=Integer.valueOf(txt_totalbayar.getText());
        jtiket=Integer.valueOf(txt_totalharga.getText());
        ttiket=htiket-jtiket;
        txt_kembalian.setText(""+ttiket);
    }//GEN-LAST:event_txt_totalbayarKeyReleased

