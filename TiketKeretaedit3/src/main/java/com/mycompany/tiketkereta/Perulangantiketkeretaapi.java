    private void jurusanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jurusanActionPerformed
        // TODO add your handling code here:
        //membuat kondisi untuk menentukan harga sesuai jurusan
        if(jurusan.getSelectedItem().equals("Pilih Jurusan")){
            buttonGroup1.clearSelection();
            txt_harga.setText("");
        }
        else if(jurusan.getSelectedItem().equals("Malang Kota")){
            Eksekutif=80000;
            Ekonomi=40000;
        }
        else if(jurusan.getSelectedItem().equals("Kediri Kota")){
            Eksekutif=50000;
            Ekonomi=25000;
        }
        else if(jurusan.getSelectedItem().equals("Surabaya")){
            Eksekutif=60000;
            Ekonomi=30000;
        }
        else if(jurusan.getSelectedItem().equals("Jakarta")){
            Eksekutif=80000;
            Ekonomi=20000;
        }
    }//GEN-LAST:event_jurusanActionPerformed

